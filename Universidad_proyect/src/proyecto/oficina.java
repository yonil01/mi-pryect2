package proyecto;




public class oficina {
    int ide;
    String nombre;

    public oficina(int ide, String nombre) {
        this.ide = ide;
        this.nombre = nombre;
    }

    public int getIde() {
        return ide;
    }

    public void setIde(int ide) {
        this.ide = ide;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
